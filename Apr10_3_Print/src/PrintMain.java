public class PrintMain {
	// 써도 되는데 수동으로 따로 실행
	public static void main(String[] args) {
		// 여기다 쓰면 자동실행
		// syso->ctrl+space

		// 중간 테스트
		// 줄 바뀌는게 가독성 낫지
		// 콘솔창에 출력하고나서 줄 바꿈
		System.out.println("sw");

		// 콘솔창에 출력
		// 따로 단축키 없음
		System.out.print("qwe");

		
		// format:형식
		// 형식 지정해서 출력할때
		System.out.printf("%.2f",3.141521312);
	}
	// 써도 되는데 아직 그걸 안배움
}
